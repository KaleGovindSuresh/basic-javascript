// logical operator

// AND - &&
//  - it evaluate to true only it both the operands are true otherwise false

console.log(false && false);
console.log(false && true);
console.log(true && false);
console.log(true && true);
console.log(true & true);  // 1

console.log(false && "abcd");
console.log("not true" && false);
console.log(true && " welcome to the true statements");


// OR - ||


console.log(false || false);
console.log(false || true);
console.log(true || false);
console.log(true || true);

console.log(false  ||"abcd");
console.log("true"  ||false);
console.log(true || " welcome to the true statements" );

// NOT - !

console.log(!true);
console.log(!false);