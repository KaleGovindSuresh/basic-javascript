// === triple equal to
// -- it compares values and thier datatypes


console.log(10 == 10);//true
console.log(10 === 10); //true

console.log(10 == "10"); // true
console.log(10 === "10"); // false

console.log(10 != 10);  // false
console.log(10 !== 10); // false

console.log(10 != "10"); //false
console.log(10 !== "10"); // true